    public function countResults()
    {
        $questions = Question::all();

        foreach ($questions as $question) {
            $question = Question::find($question->id);
            $question->da = Result::where('answers', 'like', '%q' . $question->id . '=1%')->count();
            $question->nema_misljenje = Result::where('answers', 'like', '%q' . $question->id . '=0%')->count();
            $question->ne = Result::where('answers', 'like', '%q' . $question->id . '=-1%')->count();

            $question->save();
        }

        // dd($breakdown);
    }

